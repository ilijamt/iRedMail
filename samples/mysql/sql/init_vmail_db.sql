-- Create database for virtual hosts
CREATE DATABASE IF NOT EXISTS PH_VMAIL_DB_NAME CHARACTER SET utf8;

-- Create user for virtual hosts
CREATE USER 'PH_VMAIL_DB_BIND_USER'@'PH_MYSQL_GRANT_HOST' IDENTIFIED BY 'PH_VMAIL_DB_BIND_PASSWD';
CREATE USER 'PH_VMAIL_DB_ADMIN_USER'@'PH_MYSQL_GRANT_HOST' IDENTIFIED BY 'PH_VMAIL_DB_ADMIN_PASSWD';

-- Permissions
GRANT SELECT ON PH_VMAIL_DB_NAME.* TO 'PH_VMAIL_DB_BIND_USER'@'PH_MYSQL_GRANT_HOST';
-- GRANT SELECT ON PH_VMAIL_DB_NAME.* TO 'PH_VMAIL_DB_BIND_USER'@'PH_HOSTNAME';
GRANT SELECT,INSERT,DELETE,UPDATE ON PH_VMAIL_DB_NAME.* TO 'PH_VMAIL_DB_ADMIN_USER'@'PH_MYSQL_GRANT_HOST';
-- GRANT SELECT,INSERT,DELETE,UPDATE ON PH_VMAIL_DB_NAME.* TO 'PH_VMAIL_DB_ADMIN_USER'@'PH_HOSTNAME';

FLUSH PRIVILEGES;
